//Switch Statement
const mention = 'A';

switch (mention) {
    case 'A':
        console.log('Vous avez la mention A');
        break;
    case 'B':
        console.log('Vous avez la mention B');
        break;
    case 'C':
        console.log('Vous avez la mention C');

    default:
        console.log('On en sait rien sur vous');
        break;
}